package javaloop;

public class ForDemo {

	public static void main(String[] args) {
		 for (int i = 0; i < 10; i++) {
	            System.out.println("Coding Everybody " + i);
	        }
		
		 int i = 0;
		 while(i<10){
		     System.out.println("coding everybody"+(i+1)*2);
		     i++;
		 }
	    }


}
